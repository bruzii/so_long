#include "libft.h"

int	ft_atoi(const char *str)
{
	int long	res;
	int			signe;
	int			i;

	signe = 0;
	res = 0;
	i = 0;
	while ((str[i] == ' ') || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			signe++;
		i++;
	}
	while (str[i] && (str[i] >= '0' && str[i] <= '9'))
	{
		res = res * 10 + (str[i] - '0');
		i++;
	}
	if (signe % 2 != 0)
		return (res * -1);
	return (res);
}
